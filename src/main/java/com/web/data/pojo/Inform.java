package com.web.data.pojo;

public class Inform
{
  private String informId;
  private String informType;
  private String lessID;
  private String infInfo;
  private String infTime;

  public String getInformId()
  {
    return this.informId; }

  public void setInformId(String informId) {
    this.informId = informId; }

  public String getInformType() {
    return this.informType; }

  public void setInformType(String informType) {
    this.informType = informType; }

  public String getLessID() {
    return this.lessID; }

  public void setLessID(String lessID) {
    this.lessID = lessID; }

  public String getInfInfo() {
    return this.infInfo; }

  public void setInfInfo(String infInfo) {
    this.infInfo = infInfo; }

  public String getInfTime() {
    return this.infTime; }

  public void setInfTime(String infTime) {
    this.infTime = infTime;
  }
}